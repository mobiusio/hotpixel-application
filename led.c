static uint_8 led_initialized = FALSE; // to make sure led_init gets run before systick
static uint_32 col_idx;
static uint_32 step = 0;
union led_pixel pixels[LED_SIZE_X][LED_SIZE_Y]; // to give ui.c access

//
// color mapping arrays
//
static uint_8 red_map[256];
static uint_8 green_map[256];
static uint_8 blue_map[256];


void led_init (void) {
	uint_8 all_off[] = {0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}; 
	uint_32 i;
	if (led_initialized == TRUE) {
		return; // already initialized
	}

	// set all column driver to high-Z
	/* Enable clock gating to PORTD */
  SIM_SCGC5 |= SIM_SCGC5_PORTD_MASK;
	
	PORTD_PCR0 = PORT_PCR_MUX(1);
	PORTD_PCR0 |= PORT_PCR_DSE_MASK; // high drive strength, open drain
	PORTD_PCR1 = PORT_PCR_MUX(1);
	PORTD_PCR1 |= PORT_PCR_DSE_MASK;
	PORTD_PCR2 = PORT_PCR_MUX(1);
	PORTD_PCR2 |= PORT_PCR_DSE_MASK;
	PORTD_PCR3 = PORT_PCR_MUX(1);
	PORTD_PCR3 |= PORT_PCR_DSE_MASK;
	PORTD_PCR4 = PORT_PCR_MUX(1);
	PORTD_PCR4 |= PORT_PCR_DSE_MASK;
	PORTD_PCR5 = PORT_PCR_MUX(1);
	PORTD_PCR5 |= PORT_PCR_DSE_MASK;
	PORTD_PCR6 = PORT_PCR_MUX(1);
	PORTD_PCR6 |= PORT_PCR_DSE_MASK;
	PORTD_PCR7 = PORT_PCR_MUX(1);
	PORTD_PCR7 |= PORT_PCR_DSE_MASK;
	
	GPIOD_PDDR |= 0xFF; // set all low
	GPIOD_PDOR = 0xFF; // set all low
	col_idx = 0;
	
	// init I2C device
	i2c_init();
	
	// disable all leds
	i2c_send_cmd(all_off, sizeof(all_off), RED_W_ADDR);
	i2c_send_cmd(all_off, sizeof(all_off), GREEN_W_ADDR);
	i2c_send_cmd(all_off, sizeof(all_off), BLUE_W_ADDR);
		
	// zero out pixel array
	memset(pixels, 0x00, sizeof(pixels));
	
	// compute mapping functions, seems to be a good balance
	for (i = 0; i <256; i++) {
		red_map[i]   = (uint_8)(255*pow(((float)i)/255,1.37));
		green_map[i] = (uint_8)(255*pow(((float)i)/255,1.35));
		blue_map[i]  = (uint_8)(255*pow(((float)i)/255,1.4));
	}

	led_initialized = TRUE;
}


void led_random (void) {
	// random color distribution
	
	int i, j;
	for (i = 0; i < LED_SIZE_X; i++) {
		for (j = 0; j < LED_SIZE_Y; j++) {
			pixels[i][j].pixel.red = 0xFF & rand();
			pixels[i][j].pixel.green = 0xFF & rand();
			pixels[i][j].pixel.blue = 0xFF & rand();
			
			#ifdef LED_RANDOM_HIGH_CONTRAST
			if ((pixels[i][j].pixel.red >= pixels[i][j].pixel.green) &&
				(pixels[i][j].pixel.red >= pixels[i][j].pixel.blue)) {
					pixels[i][j].pixel.green = pixels[i][j].pixel.green >> 
					LED_RANDOM_HIGH_CONTRAST_SHIFT;
					pixels[i][j].pixel.blue = pixels[i][j].pixel.blue >> 
					LED_RANDOM_HIGH_CONTRAST_SHIFT;
			}
			else if ((pixels[i][j].pixel.green >= pixels[i][j].pixel.red) &&
				(pixels[i][j].pixel.green >= pixels[i][j].pixel.blue)) {
					pixels[i][j].pixel.red = pixels[i][j].pixel.red >> 
					LED_RANDOM_HIGH_CONTRAST_SHIFT;
					pixels[i][j].pixel.blue = pixels[i][j].pixel.blue >> 
					LED_RANDOM_HIGH_CONTRAST_SHIFT;
			}
			else {
					pixels[i][j].pixel.red = pixels[i][j].pixel.red >> 
				LED_RANDOM_HIGH_CONTRAST_SHIFT;
					pixels[i][j].pixel.green = pixels[i][j].pixel.green >> 
				LED_RANDOM_HIGH_CONTRAST_SHIFT;
			}
			#endif
			
		}
	}
}

void led_symbol (uint_32 sym, uint_8 x, uint_8 y, uint_8 r, uint_8 g, uint_8 b) {
	// this function loads various symbols into the pixels frame buffer
	//
	// TODO: add a way to control offset
	// 
	static uint_8 row[LED_SIZE_Y]; // assuming 8 bits wide
	int i,j;
	
	switch (sym) {
		case 0:
			row[7] = 0x33; // 00110011
			row[6] = 0x33; // 00110011
			row[5] = 0x26; // 00100110
			row[4] = 0x2C; // 00101100
			row[3] = 0x38; // 00111000
			row[2] = 0x2C; // 00101100
			row[1] = 0x26; // 00100110
			row[0] = 0x33; // 00110011
			break;
		
		default:
			memset(row, 0x00, sizeof(row));
	}
	
	for (i = x; i < LED_SIZE_X; i++) {
		for (j = y; j < LED_SIZE_Y; j++) {
			if (row[j-y] & (0x80 >> (i-x))) {
				pixels[i][j].pixel.red = r;
				pixels[i][j].pixel.green = g;
				pixels[i][j].pixel.blue = b;
			}
			else {
				pixels[i][j].pixel.red = 0;
				pixels[i][j].pixel.green = 0;
				pixels[i][j].pixel.blue = 0;
			}
		}
	}
}

//
// screen update functions
//

void led_all_off (void) { 
	/*
	 * this function disables all cols by turning off high-side FETs
	 */
	
	GPIOD_PDOR |= 0xFFFFFFFF;
	GPIOD_PDDR &= 0xFFFFFF00;
}

void led_paint_cols (void) {
	/*
	 * this function handles turning on the current column FET
	 * and then updates the col_idx index
	 */
	
	// output current fet values
	GPIOD_PDDR = (0xFFFFFF00 | 0x1 << col_idx);
	GPIOD_PDOR = (0xFFFFFFFF & 0x0 << col_idx);

	// update column PFETS
	if (col_idx < LED_SIZE_X) {
		col_idx++;
	}
	else {
		col_idx = 0;
	}
}

void led_paint_rows (void) {
	/*
	 * this function writes the color codes to the RGB PWM drivers
	 */
	
	static uint_8 r[LED_SIZE_Y];
	static uint_8 g[LED_SIZE_Y];
	static uint_8 b[LED_SIZE_Y];
	static uint_8 cmd[] = {0x80, 0x00, 0x00, 0xFE, 0xFE, 0xFE, 0xFE, 0xFE, 0xFE, 0xFE, 0xFE, 0xFF, 0xFF, 0xAA, 0xAA}; // LED_SIZE_Y must be 8
  #if (LED_SIZE_Y != 8) 
  #error "LED_SIZE_Y is not 8!"
  #endif 

	uint_32 idx;

	// pixel map, applies to REV A,B PCB, the order in the LED driver channel is this:
  // [3] [2] [1] [0] [7] [6] [5] [4] for red
	// [4] [5] [6] [7] [0] [1] [2] [3] for green
  // [3] [2] [1] [0] [7] [6] [5] [4] for blue
  
  static const uint_8 r_map[LED_SIZE_Y] = {0x03, 0x02, 0x01, 0x00, 0x07, 0x06, 0x05, 0x04};
	static const uint_8 g_map[LED_SIZE_Y] = {0x04, 0x05, 0x06, 0x07, 0x00, 0x01, 0x02, 0x03};
	static const uint_8 b_map[LED_SIZE_Y] = {0x03, 0x02, 0x01, 0x00, 0x07, 0x06, 0x05, 0x04};
  
	if (led_initialized == FALSE) {
		return; // need to wait for led_init() to complete
	}
	
	for (idx = 0; idx < LED_SIZE_Y; idx++) {
		// remapping so origin bottom left corner, adjusting color
		r[r_map[idx]] = led_color_adjust(pixels[LED_SIZE_X-col_idx-1][LED_SIZE_Y-idx-1].pixel.red, RED_CHANNEL);
		g[g_map[idx]] = led_color_adjust(pixels[LED_SIZE_X-col_idx-1][LED_SIZE_Y-idx-1].pixel.green, GREEN_CHANNEL);
		b[b_map[idx]] = led_color_adjust(pixels[LED_SIZE_X-col_idx-1][LED_SIZE_Y-idx-1].pixel.blue, BLUE_CHANNEL);
	}
	
	memcpy((uint_8*)cmd+3, r, LED_SIZE_Y);
	i2c_send_cmd(cmd, sizeof(cmd), RED_W_ADDR);
	memcpy((uint_8*)cmd+3, g, LED_SIZE_Y);
	i2c_send_cmd(cmd, sizeof(cmd), GREEN_W_ADDR);
	memcpy((uint_8*)cmd+3, b, LED_SIZE_Y);
	i2c_send_cmd(cmd, sizeof(cmd), BLUE_W_ADDR);
}

void led_systick_handler (void) {
	/*
	 * this function is called from the systick handler
	 * and facilitates screen updating, all of these functions
	 * pull data from the pixel array
	 */
	
	switch (step) {
		case 0:
			//
			// double-buffering should be implemented here along with
		  // updating color curves since no data is transmitted 
		  // from the pixel buffer in this case
		  //
			led_all_off();
			step++;
			break;
		case 1:
			led_paint_rows();
			step++;
			break;
		case 2:
			step++;
			break;
		case 3:
			led_paint_cols();
			step = 0;
			break;
		default:
			step = 0;
	}
}

